import re


def color():
    in_col = str(input('Введите цвет в формате RGB(A): '))
    return True\
        if re.match(r'^(rgb\((\d|[1-9]\d|1\d{2}|2[0-5]{2}|\d%|[1-9]\d%|100%)(, ?(\d|[1-9]\d|1\d{2}|2[0-5]{2}|\d%|[1-9]\d%|100%)){2}\)|rgba\((\d|[1-9]\d|1\d{2}|2[0-5]{2}|\d%|[1-9]\d%|100%)(, ?(\d|[1-9]\d|1\d{2}|2[0-5]{2}|\d%|[1-9]\d%|100%)){2}, ?(0(\.\d{,2})?|1|\d%|[1-9]\d%|100%)\))$', in_col, re.I)\
        else False


print(color())
